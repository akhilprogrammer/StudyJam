
// Named function (Function have name)

function add() { 
    let x: number = 10;
    let y: number = 20;
    console.log(`sum = ${x + y}`);
}

add(); // function call



// Anonymous function (Function without name)


var x = function() { 
    let x: number = 30;
    let y: number = 50;
    console.log(`sum = ${x + y}`);
}


x(); // function call