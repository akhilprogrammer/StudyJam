
// while loop 

    // while loop first check condition
    // if condition true then body execute. this repeat until condition become false
    // when condition become false the loop exit

    let x: number = 1;

    while(x <= 10) {

        console.log(x);      
        x = x + 1;

    }


// do - while loop

    // do while loop first execute body
    // then check condition. if condition true repeat the process
    // if condition false loop exit

    let y: number = 1;

    do {

        console.log(y);     
        y = y + 1;

    } 
    while(y <= 10);