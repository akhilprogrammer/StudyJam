
// switch statements can use when we know the exact value of conditions

let carColor: string = 'pink';


switch(carColor) {

    case 'green': 
        console.log('This is a green car');
        break;

    case 'red': 
        console.log('This is a red car');
        break;

    case 'blue': 
        console.log('This is a blue car');
        break;

    case 'pink': 
        console.log('This is a pink car');
        break;

    case 'yellow': 
        console.log('This is a yellow car');
        break;

    default: 
        console.log('Sorry your color is not valid');
        break;       

}