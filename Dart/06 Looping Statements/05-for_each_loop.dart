
// for - each loop also use for iterating a collection
// on each iteration, we get each value of collection starting from index 0

// for each loop structure

  /*
  
    collection.forEach(function);

   */


void main() {

  // create a collection x

  var x = [1, 2, 3, 4, 5];

  x.forEach((item) {
    print(item);
  });


}