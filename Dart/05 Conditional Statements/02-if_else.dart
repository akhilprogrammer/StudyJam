
// if - else statements check a condition first, 
// if condition is true then execute the if part
// if condition false then execute else part

void main() {

  var x = 7;

  // here first check the condition x less than 10

  if(x < 10) {
    print('Number less than 10');         // if condition true this part execute
  }
  else {
    print('Number greater than or equal to 10');  // if condition false this part execute
  }

}