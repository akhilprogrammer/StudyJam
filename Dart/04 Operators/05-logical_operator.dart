

// logical operator  allow a program to make a decision based on multiple conditions
// which return true or false after checking conditions

void main() {

  var x = 10;

  // logical and    &&
  

  print(x > 5 && x < 10);  // if both condition is true then we get a true value




  // logical or     ||
  

  print(x > 5 || x < 10);  // if any one condition is true then we get a true value  




  // logical not    !
  

  print(!(x < 10));        // here value of condition is false , but not operator reverse its value and we get true overall 
    


}