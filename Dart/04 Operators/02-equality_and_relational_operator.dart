

// equality and relational operators used to compare two values  
// it return true or false after comparison

void main() {

    var x = 10;

    // less than operator

      print(x < 10);     // checking x is less than 10

    // greater than operator

      print(x > 10);    // checking x is greater than 10

    // equal operator

      print(x == 10);   // checking x equal to 10

    // not equal operator

      print(x != 10);   // checking x not equal to 10

    // less than or equal to operator
    
      print(x <= 10);   // checking x less than or equal to 10

    // greater than or equal to operator

      print(x >= 10);   // checking x greater than or equal to 10

}